package chapter01;		//왼편 폴더에 chapter01이라고 명확하게 써있으면 package가 써있고, defualt 뭐시기라써있으면 패키지가 안써있어 그때 억지로 패키지 쓸 필요 없음

public class Hello {	//이 큰 괄호부분에 코드를 쓰자
	public static void main(String[] args)	//	이 줄은 꼭 외우자   스트링의 S는 꼭 대문자로
	{	// {이 중괄호부터 스타트 포인트
		System.out.println("Hello, world.");	//ln = 한줄씩 찍는다   c언어에서/n이랑 같은 뜻 ㅎ
		System.out.println("Hello, world1.");	// ; 코드가 끝난다는 뜻 ㅎ
		System.out.println("Hello, world2.");	//코드는 순서대로 실행됨 
	}
}


